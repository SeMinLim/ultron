static void loadFPSMShiftOrPatterns() {
    printf("Loading shift-OR patterns (492 rules)...\n");
    for (int i = 0; i < 492; i++) {
        writeReg(39, g_soPatterns[i].patHi);
        writeReg(40, g_soPatterns[i].patLo);
        writeReg(41, g_soPatterns[i].bucketLenIdx);
    }
    printf("Shift-OR patterns loaded.\n");
}

static void loadFPSMHashTable() {
    printf("Loading FPSM hash tables (8 x 512)...\n");
    for (int l = 0; l < 8; l++) {
        for (int h = 0; h < 512; h++) {
            int word = h / 64;
            int bit  = h % 64;
            if ((g_hashTable[l][word] >> bit) & 1ULL) {
                writeReg(31, ((uint32_t)l << 9) | (uint32_t)h);
            }
        }
    }
    printf("Hash table loaded.\n");
}

static void loadRuleReductionTable() {
    printf("Loading cuckoo rule table (568 entries)...\n");
    for (int i = 0; i < 568; i++) {
        writeReg(42, g_ckEntries[i].patHi);
        writeReg(43, g_ckEntries[i].patLo);
        uint32_t ctrl = ((uint32_t)g_ckEntries[i].tableIdx << 28)
                      | ((uint32_t)g_ckEntries[i].lenIdx   << 25)
                      | ((uint32_t)g_ckEntries[i].hashIdx  << 16)
                      |  (uint32_t)g_ckEntries[i].ruleId;
        writeReg(32, ctrl);
    }
    printf("Cuckoo rule table loaded.\n");
}

static void loadPortGroupTable() {
    printf("Loading port group table (492 rules)...\n");
    for (int i = 0; i < 492; i++) {
        uint16_t ruleId = g_portGroups[i].ruleId;
        uint8_t  proto  = g_portGroups[i].proto;
        uint8_t  pgId   = g_portGroups[i].pgId;
        uint16_t srcMin = g_portGroups[i].srcMin;
        uint16_t srcMax = g_portGroups[i].srcMax;
        uint16_t dstMin = g_portGroups[i].dstMin;
        uint16_t dstMax = g_portGroups[i].dstMax;
        writeReg(33, ((uint32_t)ruleId << 16) | ((uint32_t)pgId << 8) | proto);
        writeReg(34, ((uint32_t)srcMin << 16) | srcMax);
        writeReg(35, ((uint32_t)dstMin << 16) | dstMax);
    }
    printf("Port group table loaded.\n");
}

static void loadAllPatterns() {
    printf("Configuring FPSM...\n");
    loadFPSMShiftOrPatterns();
    loadFPSMHashTable();
    loadRuleReductionTable();
    loadPortGroupTable();
    printf("FPSM configuration complete: %d rules loaded.\n", NUM_RULES);
}
